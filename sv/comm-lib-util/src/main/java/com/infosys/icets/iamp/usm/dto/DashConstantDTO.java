package com.infosys.icets.iamp.usm.dto;

import java.io.Serializable;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;

import com.fasterxml.jackson.annotation.JsonIdentityInfo;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.ObjectIdGenerators;

import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.Setter;
import lombok.ToString;

@Getter
@Setter
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class DashConstantDTO implements Serializable {

	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1L;

	/** The id. */
	@Id
	@EqualsAndHashCode.Include
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private Integer id;

	/** The project name. */
	@JsonProperty("project_name")
	private String project_name;

	/** The keys. */
	@Column(name = "item")
	private String keys;

	/** The value. */
	@Column(name = "item_value")
	private String value;

	/** The project id. */
	@JsonProperty("project_id")
	private ProjectDTO2 project_id;
	
	/** The portfolio id. */
	@JsonProperty("portfolio_id")
	private PortfolioDTO2 portfolio_id;

	/**
	 * To string.
	 *
	 * @return the string
	 */
	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "DashConstant{" + ", id='" + getId() + "'" + ", project_name='" + getProject_name() + "'" + ", keys='"
				+ getKeys() + "'" + ", value='" + getValue() + "}";
	}
}
